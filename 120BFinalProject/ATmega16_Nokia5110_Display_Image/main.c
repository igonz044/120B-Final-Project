int main()
{
	// Set Data Direction Registers
	// Buttons PORTA[0-7], set AVR PORTA to pull down logic
	DDRA = 0x00; PORTA = 0xFF; //BUTTONS
	// DDRB = 0xFF; PORTB = 0x00;
	DDRC = 0xF0; PORTC = 0x0F; // PC7..4 outputs init 0s, PC3..0 inputs init 1s
	DDRD = 0xFF; PORTD = 0x00; //LEDs
	// Period for the tasks

	//Declare an array of tasks
	static task task1, task2, task3;
	task *tasks[] = { &task1, &task2,&task3};
	const unsigned short numTasks = sizeof(tasks)/sizeof(task*);
	//There was only one task to do so the size = 1;

	task1.state = init_OS;
	task1.period = 1; //Task Period.
	task1.elapsedTime =task1.period;
	task1.TickFct = &ObstacleTick;
	
	task2.state = init_PS;
	task2.period = 1; //Task Period.
	task2.elapsedTime =task2.period;
	task2.TickFct = &PlayerTick;
	
	task3.state = init_DS;
	task3.period = 1; //Task Period.
	task3.elapsedTime =task3.period;
	task3.TickFct = &DisplayTick;

	// Set the timer and turn it on
	TimerSet(10);
	TimerOn();
	nokia_lcd_init();
	// nokia_lcd_power(1);
	// nokia_lcd_set_cursor(24,40);

	unsigned short i; // Scheduler for-loop iterator
	while(1) {
		// PINAtmp = ~PINA & 0x1C;
		for ( i = 0; i < numTasks; i++ ) {
			if ( tasks[i]->elapsedTime >= tasks[i]->period ) {
				tasks[i]->state = tasks[i]->TickFct(tasks[i]->state);
				tasks[i]->elapsedTime = 0;
			}
			tasks[i]->elapsedTime += 1;
		}
		while(!TimerFlag);
		TimerFlag = 0;
	}
	return 0;
}

