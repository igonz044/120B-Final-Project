int PlayerTick(int PS_State)
{
	// unsigned char UpDown = PINAtmp & 0x18;
	switch(PS_State)
	{
		case init_PS:
		break;
		case wait_PS:
		if(((~PINA) & 0x3C) == 0){PS_State = init_PS;}
		break;

		default:
		break;
	}

	switch(PS_State)
	{
		case init_PS:
			x = ReadADC(0);
			y = ReadADC(1);
			if(find_direction_from_xy(x,y)){ restartGame();} //reset
			if(A1){ ButtonA();} //left
			if(A2){ ButtonB();} //middle
			if(A3){ ButtonC();} //right

		break;
		case wait_PS:
		break;
		default:
		break;
	}
	return PS_State;
}



